    public PirateBay(String command) {
        String[] splitArray = command.split("\\s+");
        String argument="";
        if (splitArray.length > 0) {
            switch (splitArray[0]) {
                case "-s":
                    argument=splitArray[0];
                    s_switch = "s";
                    break;
                case "-l":
                    argument=splitArray[0];
                    s_switch = "l";
                    break;
                case "-d":
                    argument=splitArray[0];
                    s_switch = "d";
                    break;
                case "-n":
                    argument=splitArray[0];
                    s_switch = "n";
                default:
                    s_switch = "s";
                    break;
            }
            command=command.replace(argument, "");
        }
        if(command.length()>0){
        s_name=command;}
        /* For the HTTP Connection */
        url = null;
        conn = null;
        fullUrl = "";
        /* Miscelanous */
        json = "";
        /* For the Gson/Json */
        gson = new Gson();
    }

    public String getJson() {
        try {
            /* Create a URL obj from strings */
            fullUrl = (QUERY_URL.concat("?name=").concat(s_name)).replace(" ", "%20").concat("&orderby=" + s_switch);

            url = new URL(fullUrl);
            /* Debug */
            System.out.println(fullUrl);

            conn = url.openConnection();
            // Get the response
            rd = new BufferedReader(new InputStreamReader(conn.getInputStream()));
            String line = "";
            while ((line = rd.readLine()) != null) {
                json = json.concat(line);
            }
            rd.close();
        } catch (MalformedURLException ex) {
            ex.printStackTrace();
        } catch (IOException ex) {
            ex.printStackTrace();
        }
        return json;
    }

    public String getFormattedResult(boolean hasColors) {

        gson = new GsonBuilder().setPrettyPrinting().create();
        PirateBayJsonItem[] results = gson.fromJson(this.getJson(), PirateBayJsonItem[].class);

        /* Fixes NullPointerException Bug that occurs if the URL DNE */
        if(results == null) {
          return "";
        }

        String output = "";
//        System.out.println(results.length);
//        System.out.println(results.toString());
        if(hasColors) {
          for (PirateBayJsonItem result : results) {
            output += result.getColorString();
          }
        } else {
          for (PirateBayJsonItem result : results) {
            output += result.toString();
          }
        }
        return output;
    }
